// As variáveis declaradas com "let" só existem dentro do bloco que foram declaradas e permitem reatribuição de valor
let user: string = "Enzo";

// As constantes, que são declaradas com "const", só existem dentro do seu bloco e NÃO PERMITEM reatribuição de valor
const id: number = 1;

// As variáveis declaradas com "var" possuem um escopo global, e permitem reatribuição.
var instrumento: string = "Violão";